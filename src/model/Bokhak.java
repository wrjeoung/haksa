package model;

import java.util.Date;

public class Bokhak {
	private int num;
	private String bokhakgi;
	private String bokgubun;
	private String bokyear;
	private String bokyehakgi;
	private String boksayu;
	private Date bokhak_reg_date;
	private String rnum;
	private String bokchuri;
	private String name;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getBokhakgi() {
		return bokhakgi;
	}
	public void setBokhakgi(String bokhakgi) {
		this.bokhakgi = bokhakgi;
	}
	public String getBokgubun() {
		return bokgubun;
	}
	public void setBokgubun(String bokgubun) {
		this.bokgubun = bokgubun;
	}
	public String getBokyear() {
		return bokyear;
	}
	public void setBokyear(String bokyear) {
		this.bokyear = bokyear;
	}
	public String getBokyehakgi() {
		return bokyehakgi;
	}
	public void setBokyehakgi(String bokyehakgi) {
		this.bokyehakgi = bokyehakgi;
	}
	public String getBoksayu() {
		return boksayu;
	}
	public void setBoksayu(String boksayu) {
		this.boksayu = boksayu;
	}
	public Date getBokhak_reg_date() {
		return bokhak_reg_date;
	}
	public void setBokhak_reg_date(Date bokhak_reg_date) {
		this.bokhak_reg_date = bokhak_reg_date;
	}
	public String getRnum() {
		return rnum;
	}
	public void setRnum(String rnum) {
		this.rnum = rnum;
	}
	public String getBokchuri() {
		return bokchuri;
	}
	public void setBokchuri(String bokchuri) {
		this.bokchuri = bokchuri;
	}
}
