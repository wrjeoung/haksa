package model;

import java.util.Date;

public class Janghak {
	private int num;
	private String janghakbank;
	private String janghakbanknum;
	private String janghakname;
	private String janghakmenu;
	private String janghakmy;
	private String janghakchuri;
	private Date janghak_reg_date;
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getJanghakbank() {
		return janghakbank;
	}
	public void setJanghakbank(String janghakbank) {
		this.janghakbank = janghakbank;
	}
	public String getJanghakbanknum() {
		return janghakbanknum;
	}
	public void setJanghakbanknum(String janghakbanknum) {
		this.janghakbanknum = janghakbanknum;
	}
	public String getJanghakname() {
		return janghakname;
	}
	public void setJanghakname(String janghakname) {
		this.janghakname = janghakname;
	}
	public String getJanghakmenu() {
		return janghakmenu;
	}
	public void setJanghakmenu(String janghakmenu) {
		this.janghakmenu = janghakmenu;
	}
	public String getJanghakmy() {
		return janghakmy;
	}
	public void setJanghakmy(String janghakmy) {
		this.janghakmy = janghakmy;
	}
	public String getJanghakchuri() {
		return janghakchuri;
	}
	public void setJanghakchuri(String janghakchuri) {
		this.janghakchuri = janghakchuri;
	}
	public Date getJanghak_reg_date() {
		return janghak_reg_date;
	}
	public void setJanghak_reg_date(Date janghak_reg_date) {
		this.janghak_reg_date = janghak_reg_date;
	}
}
